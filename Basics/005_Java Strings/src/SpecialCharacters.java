public class SpecialCharacters {
    public static void main(String[] args) {
        System.out.println("Hari jum\'ad"); // use \' for a single quote
        System.out.println("Dia berkata \"itu td mungkin\""); // use \"" for a double quote
        System.out.println("Nama\n: Benony"); // use \n for new line
        System.out.println("Umur\t: 19"); // use \t for TAB
        System.out.println("merah\\putih"); // use \\ for backslash
    }
}
