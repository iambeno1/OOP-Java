public class _06_Boolean {
    public static void main(String[] args) {
        // Tipe Boolean
        /*Sering kali dalam pemrograman, Anda memerlukan tipe data yang hanya dapat memiliki satu dari dua nilai, seperti:

        YES / NO
        ON / OFF
        TRUE / FALSE
        Untuk hal ini, Java memiliki tipe data boolean, yang hanya bisa menerima nilai benar atau salah: */

        boolean isJavaFun = true;
        boolean isFishTasty = false;

        System.out.println(isJavaFun);
        System.out.println(isFishTasty);
        System.out.println(!isJavaFun); // output false karena pake not / '!'
    }
}
