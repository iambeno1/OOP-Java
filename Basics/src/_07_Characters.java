public class _07_Characters {
    public static void main(String[] args) {
        // Characters
        // Tipe data char digunakan untuk menyimpan karakter tunggal. Karakter harus dikelilingi oleh tanda kutip tunggal, seperti 'A' atau 'c':

        char grade = 'A';
        System.out.println(grade);

        // Atau, jika Anda sudah terbiasa dengan nilai ASCII, Anda dapat menggunakannya untuk menampilkan karakter tertentu:
        char ch1 = 65, ch2 = 66, ch3 = 67;
        System.out.println(ch1);
        System.out.println(ch2);
        System.out.println(ch3);
        System.out.println(ch1+ch2+ch3);


        // Strings
        // Tipe data String digunakan untuk menyimpan urutan karakter (teks). Nilai string harus diapit oleh tanda kutip ganda:
        
        String hello = "Hello String";
        System.out.println(hello);

    }
}
