public class _0_Intro {
    public static void main(String[] args) throws Exception {
        System.out.println("Belajar Tipe Data Java");
        System.out.println("Java memiliki tipe data yang mirip dengan C++\n");

        System.out.println("Tipe data Integer (ukurannya sama dengan C++):");
        System.out.println("- Byte");
        System.out.println("- Short");
        System.out.println("- Int ");
        System.out.println("- Long");

        int umur = 19;
        int tahun = 2004;
        
        System.out.println("\nTipe data Float (ukurannya sama dengan C++):");
        System.out.println("- Float");
        System.out.println("- Double");

        float harga = 5.2F; // 'F' artinya float

        // Kalau double tdk perlu pake f
        double ipk = 3.7;
        System.out.println("IPK = "+ipk);
        
        System.out.println("\nTipe data Char :");
        char nilai = 'A';
        System.out.println("Nilai : "+ nilai);

        System.out.println("\nTipe data Boolean :");
        boolean benar = true;
        boolean salah = false;
        System.out.println("Benar = "+ benar);
        System.out.println("Salah = "+ salah);
        
        System.out.println("\nTipe data String :");
        String nama = "Benony Gabriel";
        System.out.println("Nama : "+nama);
        System.out.println("Umur : "+umur);
        System.out.println("Tahun lahir : "+tahun);
        System.out.println("Harga batagor : "+harga);

        
    }
}
